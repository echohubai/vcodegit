public class PictureModel {
    private Long pid;
    private String bgPicture;
    private String fullPicture;
    private String slicePicture;
    private String pictureWidth;
    private String pictureHeight;
    private String sliceLeftTopX;
    private String sliceLeftTopY;

    public Long getPid() {
        return pid;
    }

    public void setPid(Long pid) {
        this.pid = pid;
    }

    public String getBgPicture() {
        return bgPicture;
    }

    public void setBgPicture(String bgPicture) {
        this.bgPicture = bgPicture;
    }

    public String getFullPicture() {
        return fullPicture;
    }

    public void setFullPicture(String fullPicture) {
        this.fullPicture = fullPicture;
    }

    public String getSlicePicture() {
        return slicePicture;
    }

    public void setSlicePicture(String slicePicture) {
        this.slicePicture = slicePicture;
    }

    public String getPictureWidth() {
        return pictureWidth;
    }

    public void setPictureWidth(String pictureWidth) {
        this.pictureWidth = pictureWidth;
    }

    public String getPictureHeight() {
        return pictureHeight;
    }

    public void setPictureHeight(String pictureHeight) {
        this.pictureHeight = pictureHeight;
    }

    public String getSliceLeftTopX() {
        return sliceLeftTopX;
    }

    public void setSliceLeftTopX(String sliceLeftTopX) {
        this.sliceLeftTopX = sliceLeftTopX;
    }

    public String getSliceLeftTopY() {
        return sliceLeftTopY;
    }

    public void setSliceLeftTopY(String sliceLeftTopY) {
        this.sliceLeftTopY = sliceLeftTopY;
    }


}
